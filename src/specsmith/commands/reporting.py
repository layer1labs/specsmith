@click.command(name="trace-score")
@click.option("--project-dir", type=click.Path(exists=True), default=".")
@click.option("--json", "as_json", is_flag=True, default=False)
@click.option("--ci", "ci_mode", is_flag=True, default=False)
@click.option("--threshold", type=float, default=80.0)
def trace_score_cmd(project_dir: str, as_json: bool, ci_mode: bool, threshold: float) -> None:
    payload = _calculate_trace_score(Path(project_dir).resolve())
    if as_json:
        click.echo(json.dumps(payload, indent=2))
    else:
        click.echo(f"trace score: {payload['score']:.2f}/100")
        click.echo("breakdown:")
        for key, value in payload["metrics"].items():
            click.echo(f"- {key}: {value:.2f}")
        click.echo("recommendations:")
        for row in payload["recommendations"]:
            click.echo(f"- {row}")
    if ci_mode and payload["score"] < threshold:
        raise SystemExit(1)


def register_reporting_commands(main: click.Group) -> None:
    main.add_command(quickstart_cmd)
